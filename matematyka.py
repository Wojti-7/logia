from math import *

print('2 ** 3 =', 2**3)
print('8 % 3 =', 8%3)

print('sqrt(2.0) =', sqrt(2.0))

print('floor(2.3) =', floor(2.3))
print('floor(-2.3) =', floor(-2.3))
print('ceil(2.3) =', ceil(2.3))
print('ceil(-2.3) =', ceil(-2.3))
print('trunc(2.3) =', trunc(2.3))
print('trunc(-2.3) =', trunc(-2.3))

print('factorial(5) =', factorial(5))

print('gcd(12, 16) =', gcd(12, 16))
print('lcm(12, 16) =', lcm(12, 16))

print('sum([2, 3, 5]) =', sum([2, 3, 5]))
print('min([2, 3, 5]) =', min([2, 3, 5]))
print('max([2, 3, 5]) =', max([2, 3, 5]))
print('prod([2, 3, 5]) =', prod([2, 3, 5]))

from random import *

print('randrange(0, 10, 1) =', randrange(0, 10, 1))
print('randrange(0, 10, 2) =', randrange(0, 10, 2))

print('sample(range(100), k=5) =', sample(range(100), k=5))

d = 'ace two three four'.split()
print('d =', d)
print('shuffle(d)')
shuffle(d)
print('d =', d)

