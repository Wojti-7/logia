def przesun(x, y):
    up()
    fd(x)
    lt(90)
    fd(y)
    rt(90)
    down()

def fd2(l):
    fillcolor('black')
    begin_fill()
    for i in range(1, 3, 1):
        fd(l)
        rt(90)
        fd(4)
        rt(90)
    end_fill()
    fd(l)
    
