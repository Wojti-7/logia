from turtle import *
from math import sqrt

def kwad(bok, kolor):
    fillcolor(kolor)
    begin_fill()
    up()
    fd(bok/2)
    down()
    rt(90)
    fd(bok/2)
    rt(90)
    fd(bok)
    rt(90)
    fd(bok)
    rt(90)
    fd(bok)
    rt(90)
    fd(bok/2)
    rt(90)
    end_fill()
    up()
    fd(bok/2)
    down()
    rt(180)

def warstwa(bok, kolor, przesun):
    # pierwszy
    up()
    fd(przesun)
    lt(90)
    fd(przesun)
    down()
    kwad(bok, kolor)    
    up()
    bk(przesun)
    rt(90)
    bk(przesun)
    down()
    # drugi
    up()
    fd(przesun)
    rt(90)
    fd(przesun)
    down()
    kwad(bok, kolor)    
    up()
    bk(przesun)
    lt(90)
    bk(przesun)
    down()
    # trzeci
    up()
    bk(przesun)
    lt(90)
    fd(przesun)
    down()
    kwad(bok, kolor)    
    up()
    bk(przesun)
    rt(90)
    fd(przesun)
    down()
    # czwarty
    up()
    bk(przesun)
    rt(90)
    fd(przesun)
    down()
    kwad(bok, kolor)    
    up()
    bk(przesun)
    lt(90)
    fd(przesun)
    down()

speed(10)

kwad(400, 'white')
warstwa(400/6, 'green', 1000/6)
warstwa(400/3, 'yellow', 200/2)
kwad(400/2, 'green')

done()
